#include "libft.h"

char	*ft_strdup(const char *s1)
{
	int		size;
	int		i;
	char	*dup;

	i = 0;
	size = ft_strlen(s1);
	dup = (char*)malloc(sizeof(char) * (size + 1));
	if (dup == NULL)
		return (NULL);
	while (s1[i] != '\0')
	{
		dup[i] = s1[i];
		i++;
	}
	dup[i] = '\0';
	return (dup);
}
