#include "libft.h"

int		ft_digit_count(int n)
{
	int	count;

	count = 0;
	if (n < 0)
		n = -n;
	if (n == -2147483648)
		return (10);
	if (n == 0)
		return (1);
	while (n > 0)
	{
		count++;
		n = n / 10;
	}
	return (count);
}
